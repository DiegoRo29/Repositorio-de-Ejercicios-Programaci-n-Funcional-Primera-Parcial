def suma(a:int, b:int)->int: return a+b
def resta(a:int, b:int)->int: return a-b
def multiplicacion(a:int, b:int)->int: return a*b
def division(a:int, b:int)->int: return a/b
def cuadrado(a:int)->float: return a*a

if __name__ == "__main__":
    print(suma(4,5))
    print(resta(4,5))
    print(multiplicacion(4,5))
    print(division(4,5))
    print(cuadrado(4))